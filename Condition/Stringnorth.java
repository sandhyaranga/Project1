package Condition;

public class Stringnorth {
	public static void main (String [] args) {
			String a="North";
			char c=a.charAt(0);
			if (c=='N') {
				System.out.println("North");
			}
	else if(c=='S') {
		System.out.println("South");
	}
	else if(c=='E') {
		System.out.println("East");
	}
	else if(c=='W') {
		System.out.println("West");
	}
	else {
		System.out.println("Invalid output");
	}
	}
}
			
		
			
	


