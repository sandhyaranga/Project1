package encapsulation;

public class Employee {
	private String name;
	private int id;
	private int salary;
	public void setName(String name) {
		this.name=name;
	}
	public String getName() {
		return name;
	}
	public void setId(int id) {
		this.id=id;
	}
	public int getId() {
		return id;
	}
	public void setsalary (int salary) {
		this.salary=salary;
	}
	public int getSalary() {
		return salary;
	}
}
	
	


